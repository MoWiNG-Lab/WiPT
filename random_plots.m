function weightedCountAccPlot(rand, seq)
    function [res] = sortedApe(x)
        res=zeros(height(x), 1);
        for j=1:height(x)
            res(j) = abs(x(j,1)-x(j,2))/x(j,1);
        end
        res = sort(res);
    end

    figure; hold on;
    plot(sortedApe(rand), 'Marker','x', 'LineWidth', 1.5);
    plot(sortedApe(seq), 'Marker','^', 'LineWidth', 1.5);
    set(gca, 'FontSize', 12);%, 'xtick', 0:2:20
    xlabel('Sorted Segments'); ylabel('Relative Deviation');
end




